                    <p>Chính sách bảo hành</p>
                    <p>I.Khách hàng có quyền bảo hành tại Hyundai Hải Phòng được hưởng chế độ bảo hành theo chính sách
                        theo quy định. Bao gồm:</p>
                    <ol>
                        <li>Các điều khoản bảo hành này được áp dụng cho xe ô tô được Hyundai Hải Phòng phân phối.</li>
                        <li>Hyundai Hải Phòng chịu trách nhiệm bảo hành cho mỗi chiếc xe mới được phân phối có các khiếm
                            khuyết do vật liệu hay sản xuất trong điều kiện sử dụng và bảo dưỡng bình thường trong 2 năm
                            hoặc 50.000 km, tùy điều kiện nào đến trước.</li>
                        <li>Hyundai Hải Phòng sẽ tiến hành sửa chữa hay thay thế miễn phí các phụ tùng được kết luận là
                            có sai hỏng trong giới hạn bảo hành. Tất cả các phụ tùng sai hỏng được thay thế bằng phụ
                            tùng mới theo điều kiện bảo hành sẽ trở thành tài sản của Hyundai Hải Phòng.</li>
                        <li>Ắc quy: Thời hạn bảo hành bình ắc quy là 12 tháng hoặc 20.000 km tùy theo điều kiện nào đến
                            trước.</li>
                        <li>Tất cả các phụ kiện lắp đặt tại các Đại lý như: đài, ổ CD, ổ DVD, ….bảo hành dựa theo điều
                            kiện bảo hành của nhà cung cấp phụ kiện.</li>
                    </ol>
                    <p><a href="../../all-new-grand-i10-va-ban-cu-grand-i10-co-gi-tot-hon/2-6/index.html"
                            rel="attachment wp-att-13119"><img fetchpriority="high" decoding="async"
                                class="aligncenter size-full wp-image-13119"
